#include <cub3d.h>

int	space_line(char	*str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if ((str[i] >= 7 && str[i] <= 13) || str[i] == ' ')
			return (0);
	}
	return (1);
}
